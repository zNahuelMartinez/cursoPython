#Operador de asignacion
miVariable = 10 #a esta variable le asigne el valor de 10
print(miVariable)

#Operador de reasignacion
miVariable = miVariable + 5 #reasignando la variable (deberia dar 15)
print(miVariable)
#tambien se puede hacer de esta manera
miVariable += 1 #la diferencia es que aca le suma uno a la reasignacion
print(miVariable)
#miVariable = miVariable - 2
miVariable -= 2
print(miVariable)

#esto se puede hacer con todos los operadores aritmeticos

miVariable *= 2
print(miVariable)
miVariable /= 3
print(miVariable)
miVariable //= 3
print(miVariable)